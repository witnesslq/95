package com.dhcc.modal.system;

public class Tscomponent {

	private String id;// 组件id
	
	private String title;// 组件名称
	
	private String url;// 组件url
	
	private String height;// 组件高度
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getHeight() {
		return height;
	}
	public void setHeight(String height) {
		this.height = height;
	}
	
	

}
